#include "vmth.h"

/*
**	Скалярной произведением двух, 2D, векторов
*/

t_v2d		vec_2mul(t_v2d v1, t_v2d v2)
{
	return ((t_v2d){v1.x * v2.x, v1.y * v2.y});
}
