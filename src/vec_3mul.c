#include "vmth.h"

/*
**	Скалярной произведением двух, 3D, векторов
*/

t_v3d		vec_3mul(t_v3d v1, t_v3d v2)
{
	return ((t_v3d){v1.x * v2.x, v1.y * v2.y, v1.z * v2.z});
}
